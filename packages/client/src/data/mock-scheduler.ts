// Helper to generate recent execution pattern
function generateRecentExecutions(
	successRate: number,
	count = 20,
): ExecutionStatus[] {
	const executions: ExecutionStatus[] = [];
	for (let i = 0; i < count; i++) {
		executions.push(Math.random() * 100 < successRate ? "success" : "failed");
	}
	return executions;
}

// Helper to generate execution history
function generateExecutionHistory(
	workflowId: string,
	totalRuns: number,
	successRate: number,
	daysBack = 30,
): WorkflowExecution[] {
	const executions: WorkflowExecution[] = [];
	const now = new Date();
	const failureCategories: FailureCategory[] = [
		"timeout",
		"auth_permission",
		"rate_limit",
		"validation",
		"downstream_5xx",
		"unknown",
	];

	for (let i = 0; i < Math.min(totalRuns, 100); i++) {
		const timestamp = new Date(
			now.getTime() - (i * (daysBack * 24 * 60 * 60 * 1000)) / totalRuns,
		);
		const isSuccess = Math.random() * 100 < successRate;

		executions.push({
			id: `exec-${workflowId}-${i}`,
			workflowId,
			timestamp: timestamp.toISOString(),
			status: isSuccess ? "success" : "failed",
			durationMs: Math.floor(Math.random() * 30000) + 1000,
			failureReason: isSuccess
				? undefined
				: "Execution failed due to external dependency",
			failureCategory: isSuccess
				? undefined
				: failureCategories[
						Math.floor(Math.random() * failureCategories.length)
					],
			logsUrl: `/logs/executions/${workflowId}/${i}`,
		});
	}

	return executions;
}

