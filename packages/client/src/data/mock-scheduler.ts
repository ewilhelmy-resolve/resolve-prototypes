export function getMockWorkflowDetail(
	workflowId: string,
): WorkflowDetail | null {
	const workflow = MOCK_SCHEDULED_WORKFLOWS.find((w) => w.id === workflowId);
	if (!workflow) return null;

	const successRate =
		workflow.totalRuns > 0
			? (workflow.successCount / workflow.totalRuns) * 100
			: 100;

	const executions = generateExecutionHistory(
		workflowId,
		workflow.totalRuns,
		successRate,
	);

	// Calculate failure breakdown
	const failureBreakdown: Record<FailureCategory, number> = {
		timeout: 0,
		auth_permission: 0,
		rate_limit: 0,
		validation: 0,
		downstream_5xx: 0,
		unknown: 0,
	};

	executions.forEach((exec) => {
		if (exec.failureCategory) {
			failureBreakdown[exec.failureCategory]++;
		}
	});

	return {
		...workflow,
		executions,
		failureBreakdown,
	};
}
